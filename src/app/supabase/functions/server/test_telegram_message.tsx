// Тест для проверки форматирования сообщений Telegram

// Вариант 1: С одинарным экранированием \n (правильный для JavaScript строк)
const message1 = `🆕 Новая заявка\n\n` +
  `📱 Телефон: +79991234567\n` +
  `🏢 Компания: ООО "Тест"\n` +
  `🔗 Ссылка: https://t.me/+79991234567`;

console.log('Вариант 1 (одинарное экранирование):');
console.log(message1);
console.log('---');

// Вариант 2: С двойным экранированием \\n (будет выведено как текст "\\n")
const message2 = `🆕 Новая заявка\\n\\n` +
  `📱 Телефон: +79991234567\\n` +
  `🏢 Компания: ООО "Тест"\\n` +
  `🔗 Ссылка: https://t.me/+79991234567`;

console.log('Вариант 2 (двойное экранирование):');
console.log(message2);
console.log('---');

// Для Telegram API нужно использовать вариант 1 (одинарное экранирование \n),
